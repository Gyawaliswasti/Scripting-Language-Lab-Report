<?php

// Function to calculate the sum of two numbers
function addNumber($x, $y) {
    return $x + $y;
}
// Example usage
$num1 = 5; // First number
$num2 = 3; // Second number

$sum = addNumber($num1, $num2); // Calling the function

// Display the result
echo "The sum of $num1 and $num2 is: $sum";
?>
